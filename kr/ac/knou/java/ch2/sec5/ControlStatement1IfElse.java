package kr.ac.knou.java.ch2.sec5;

public class ControlStatement1IfElse {
    public static void main(String[] args) {
        int a = 2;
        int b = 2;
        if (a == 1)
            if (b == 2)
                System.out.println("a was 2 and b was 2.");
            else
                System.out.println("a was not 1.");
    }
}
